/*
TODO Toán tử số học
* Cộng --> +
* Trừ --> -
* Nhân --> *
* Lũy thừa --> **
* Chia --> /
* Chia lấy số dư --> %
* Tăng 1 giá trị số --> ++
* Giảm 1 giá trị số --> --
*/

var a = 5;
var b = 3;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a ** b);
console.log(a / b);
console.log(a % b);
console.log(a++);
console.log(a--);
